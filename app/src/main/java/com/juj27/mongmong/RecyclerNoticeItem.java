package com.juj27.mongmong;

public class RecyclerNoticeItem {
    String ImgUrl;
    String Day;
    String Say;
    String Time;

    public RecyclerNoticeItem() {
    }

    public RecyclerNoticeItem(String imgUrl, String day, String say, String time) {
        ImgUrl = imgUrl;
        Day = day;
        Say = say;
        Time = time;
    }
}
