package com.juj27.mongmong;

public class RequestItem {

     String title;
     String img;
     String msg;
     String price;

    public RequestItem() {
    }

    public RequestItem(String title, String img, String msg, String price) {
        this.title = title;
        this.img = img;
        this.msg = msg;
        this.price = price;
    }
}
